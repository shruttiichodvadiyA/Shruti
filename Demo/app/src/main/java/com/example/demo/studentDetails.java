package com.example.demo;

public class studentDetails {

    int id;
    String name;
    float pr;

    void setId(int id) {
        this.id = id;
    }

    int getId() {
        return id;
    }

    void setName(String name) {
        this.name = name;
    }

    String getName() {
        return name;
    }

    void setPr(float pr) {
        this.pr = pr;
    }

    float getPr() {
        return pr;
    }
}
